    private void performSearch() {
        String queryStr = query.getText();
       
         Pret pret = new Pret();

        Object[][] tableData=null;
    try {
        tableData = pret.getPretsRetard(queryStr,selectedCriteria);
    } catch (ParseException ex) {
        Logger.getLogger(SearchPretUI.class.getName()).log(Level.SEVERE, null, ex);
    }
        pretTable.setModel(new javax.swing.table.DefaultTableModel(
            tableData,
            new String[]{"Id","CIN","Type","Date pret","Date Retour"}
                
        ));
                TableColumnModel columnModel = pretTable.getColumnModel();
                columnModel.getColumn(0).setPreferredWidth(5);
    }
